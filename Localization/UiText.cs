    public static readonly string[] Supported = ["ru", "be", "en"];

    public static string Lang(HttpContext? ctx)
    {
        var raw = ctx?.Request?.Cookies?["culture"];
        if (string.IsNullOrWhiteSpace(raw)) return "ru";

        var v = raw.Trim().ToLowerInvariant();

        // RequestLocalization cookie can look like: "c=ru|uic=ru"
        // Sometimes user code also writes plain "ru".
        if (v.Contains("uic=", StringComparison.Ordinal))
        {
            var start = v.IndexOf("uic=", StringComparison.Ordinal);
            if (start >= 0)
            {
                var part = v[(start + 4)..];
                var cut = part.IndexOf('|');
                v = cut >= 0 ? part[..cut] : part;
            }
        }
        else if (v.StartsWith("c=", StringComparison.Ordinal))
        {
            var part = v[2..];
            var cut = part.IndexOf('|');
            v = cut >= 0 ? part[..cut] : part;
        }

        if (v.StartsWith("en", StringComparison.Ordinal)) return "en";
        if (v.StartsWith("be", StringComparison.Ordinal)) return "be";
        return "ru";
    }

    public static bool Is(HttpContext? ctx, string lang) =>
        string.Equals(Lang(ctx), lang, StringComparison.OrdinalIgnoreCase);

    public static string T(HttpContext? ctx, string key) => T(key, Lang(ctx));

    public static string T(string key, string lang)
    {
        if (string.IsNullOrWhiteSpace(key)) return "";
        var safe = lang is "be" or "en" or "ru" ? lang : "ru";

        if (_t.TryGetValue(safe, out var dict) && dict.TryGetValue(key, out var value))
            return value;
        if (_t["ru"].TryGetValue(key, out var fallback))
            return fallback;
        return key;
    }

